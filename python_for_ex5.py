# 2 * 1 = 2   3 * 1 = 3   4 * 1 = 4
# 2 * 2 = 4   3 * 2 = 6   4 * 2 = 8
# 2 * 3 = 6   3 * 3 = 9   4 * 3 =12
# 2 * 4 = 8   3 * 4 =12   4 * 4 =16
# 2 * 5 =10   3 * 5 =15   4 * 5 =20
# 2 * 6 =12   3 * 6 =18   4 * 6 =24
# 2 * 7 =14   3 * 7 =21   4 * 7 =28
# 2 * 8 =16   3 * 8 =24   4 * 8 =32
# 2 * 9 =18   3 * 9 =27   4 * 9 =36

# 9행, 3열 : 2중 for문
for R in range(1, 10):
   for C in range(2, 4):
        print(f'{C:2} *{R:2} ={C*R:2}', end = '  ')
   else:
       C += 1 
       print(f'{C:2} *{R:2} ={C*R:2}')
