a = (6, 1, 5, 2, 7, 4, 3)

# sum
print(sum(a)) # iterable
print(sum(a, 100))

# len
print(len(a)) # container

# min, max
print(min(a)) # iterable
print(min(3, 5, 2, 20, 8)) # arg1, arg2, ...
print(max(a)) # iterable
print(max(3, 5, 2, 20, 8)) # arg1, arg2, ...

# round
print(round(10/3, 2))
print(round(3.336, 2))
print(round(12345, -2))
print(round(12355, -2))
